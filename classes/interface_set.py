""" Interface of set """

class ISet():
    """ Interface of set """
    def add(self, val):
        """ Add element in set """

    def remove(self, val):
        """ Remove element from set """

    def contains(self, val):
        """ Return true or node if contains, else False or None """

    def clear(self):
        """ Clear set """

    def count(self):
        """ Get count of elements in set """

    def is_empty(self):
        """ Return true if not empty, else False """
